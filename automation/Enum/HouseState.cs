namespace Automation.Enum;

/// <summary>
/// Represents the different states of the house.
/// </summary>
public enum HouseState
{
    /// <summary>
    /// The state of the house in the morning.
    /// </summary>
    Morning,

    /// <summary>
    /// The state of the house during the day.
    /// </summary>
    Day,

    /// <summary>
    /// The state of the house in the evening.
    /// </summary>
    Evening,

    /// <summary>
    /// The state of the house at night.
    /// </summary>
    Night
}